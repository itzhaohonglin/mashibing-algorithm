package com.howliked.mashibing.algorithm.class03;

public class ListNode {
    public int value;
    public ListNode next;

    public ListNode(int v) {
        value = v;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "value=" + value +
                ", next=" + next +
                '}';
    }
}
